package com.bridgelabz;

public class DefaultValues {
    byte b;
    short s;
    int i;
    long l;
    float f;
    double d;
    boolean bl;
    char ch;

    public static void main(String[] args) {
        DefaultValues defaultValues = new DefaultValues();
        System.out.println("Default value of Byte is: "+defaultValues.b);
        System.out.println("Default value of Short is: "+defaultValues.s);
        System.out.println("Default value of Integer is: "+defaultValues.i);
        System.out.println("Default value of Long is: "+defaultValues.l);
        System.out.println("Default value of Float is: "+defaultValues.f);
        System.out.println("Default value of Double is: "+defaultValues.d);
        System.out.println("Default value of Boolean is: "+defaultValues.bl);
        System.out.println("Default value of Char is: "+defaultValues.ch);

    }
}
